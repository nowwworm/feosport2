export default function DocsPage() {
  const [activeSection, setActiveSection] = useState('feosport');
  const [openItem, setOpenItem] = useState(null);

  const section = SECTIONS.find(s => s.id === activeSection);

  return (
    <div className="docs-page">
      <Header title="Документация" />

      <div className="docs-page__content">
        {/* ── Навигация по разделам ────────────────────────────────────── */}
        <div className="docs-page__tabs">
          {SECTIONS.map(s => (
            <button
              key={s.id}
              className={`docs-page__tab${activeSection === s.id ? ' docs-page__tab--active' : ''}`}
              onClick={() => { setActiveSection(s.id); setOpenItem(null); }}
            >
              <span className="docs-page__tab-icon">{s.icon}</span>
              {s.label}
            </button>
          ))}
        </div>

        {section && (
          <div className="docs-page__section">
            <h2 className="docs-page__section-title">{section.title}</h2>
            {section.note && (
              <p className="docs-page__note">{section.note}</p>
            )}

            <div className="docs-page__items">
              {section.items.map((item, idx) => (
                <div
                  key={idx}
                  className={`docs-page__item${openItem === idx ? ' docs-page__item--open' : ''}`}
                  onClick={() => setOpenItem(openItem === idx ? null : idx)}
                >
                  <div className="docs-page__item-header">
                    <span className="docs-page__item-icon">{item.icon}</span>
                    <div className="docs-page__item-meta">
                      <span className="docs-page__item-title">{item.title}</span>
                      <span className="docs-page__item-roles">{item.roles}</span>
                    </div>
                    {item.path && (
                      <span className="docs-page__item-path">{item.path}</span>
                    )}
                    <span className="docs-page__chevron">
                      {openItem === idx ? '▲' : '▼'}
                    </span>
                  </div>

                  {openItem === idx && (
                    <div className="docs-page__item-body">
                      {item.desc.split('\n\n').map((para, i) => (
                        <p key={i}>{para}</p>
                      ))}
                    </div>
                  )}
                </div>
              ))}
            </div>
          </div>
        )}
      </div>
    </div>
  );
}
